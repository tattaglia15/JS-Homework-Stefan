function calculateAge(currentYear, birthYear) {
    let calculateAge = (currentYear - birthYear)
    return calculateAge
}
let myAge = calculateAge(2021,1992)
alert(`You are ${myAge} years old`)
let age1 = calculateAge(2021, 1996)
alert(`You are ${age1} years old`)
let age2 = calculateAge(2021, 1999)
alert(`You are ${age2} years old`)
